    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
         $user = User::find($id);
         $roles = $user->roles()->get();
         $all_roles = Role::all();
         return view('pages.admin_pages.user')
         ->with('user',$user)
         ->with('roles',$roles)
         ->with('all_roles',$all_roles);
    }

